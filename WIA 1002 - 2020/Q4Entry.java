public class Q4Entry {
    private String key;
    private String value;
    
    public Q4Entry(String key, String value){
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }
    
    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
    
}
